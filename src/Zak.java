public class Zak {
    private String name;
    private int id;
    private int vek;

    public Zak(String name, int id, int vek) {
        this.name = name;
        this.id = id;
        this.vek = vek;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getVek() {
        return vek;
    }

    public void setVek(int vek) {
        this.vek = vek;
    }
}
